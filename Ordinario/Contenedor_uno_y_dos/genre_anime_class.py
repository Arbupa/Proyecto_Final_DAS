class Genre_Anime():
	def __init__(self, id, title, image, episodes, airing, tipo, start_date, end_date, members, rated) -> None:
		self.id = id
		self.title = title
		self.image = image
		self.episodes = episodes
		self.airing = airing
		self.tipo = tipo
		self.start_date = start_date
		self.end_date = end_date
		self.members = members
		self.rated = rated